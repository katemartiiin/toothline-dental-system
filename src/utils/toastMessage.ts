import { toast } from 'react-toastify';

export interface FieldError {
  field: string;
  message: string;
};

export const toastError = (message: string) => {
    toast.error(message, {
      position: "top-right",
      autoClose: 3000,
      hideProgressBar: false,
      closeOnClick: false,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
      theme: "colored",
    });
}

